    Module._module_list_objs2(this.v, obs.v, maxMag, 0, g_module_list_obj2);
    for (let i = 0; i < g_ret.length; i++) {
      let obj = new SweObj(g_ret[i]);
      if (filter(obj)) {
        obj.retain();
        ret.push(obj);
      }
    }
    return ret;
